package cetvrtakN2;

public class N2Niz {


    public static void main(String[] args) {


        int[] niz = {1, 2, 3, 4, 6};

        {
            int sum = 0;

            for (int i = 0; i < niz.length; i++) {
                sum += niz[i];
            }
            System.out.println("Sum je" + sum);
        }


    }


}
