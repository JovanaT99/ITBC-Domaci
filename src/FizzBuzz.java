public class FizzBuzz {


    public static double average(int x, int y) { //potpis funkcije

        return (x + y) / 2.0;
    }

    public static void main(String[] args) {

        int ocena1 = 5;
        int ocena2 = 2;
        double prosek = average(ocena1, ocena2);


        System.out.println(prosek);


    }


}